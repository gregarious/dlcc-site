    <h1>Building History</h1>

    <p>What began as a vision by a group of determined government, corporate and non-profit foundation leaders became a reality in September 2003 with the official dedication ceremony of the David L. Lawrence Convention Center. The opening of the 1.5 million square foot building was so anxiously anticipated by the convention and exposition industry that the building hosted many large trade shows while still under construction.</p>

    <p>When a city offers an overall package that includes ease of transportation, affordable lodging, distinctive dining options and diverse leisure activities, everyone wants to plan their meetings, conventions, trade shows and events there. That’s the Pittsburgh package, but prior to 2003, the Center’s space was limited.</p>

    <h2>Building Project Timeline</h2>

    <h3>1990</h3>
    <p>A study was released showing that expansion of the David L. Lawrence Convention Center was necessary in order for the region to remain competitive as a convention site.
    Pittsburgh and Allegheny County officials requested $150 million from the Commonwealth of Pennsylvania for expansion of the David L. Lawrence Convention Center.</p>

    <h3>1996</h3>
    <p>Elected officials agreed to expand the convention center by tripling the amount of exhibit space.</p>

    <h3>1998</h3>
    <p>In March, Mayor Tom Murphy and the Allegheny County Commissioners unveiled so called "Plan B" which dedicated hotel taxes and a portion of the county-wide sales tax to the convention center project.</p>

    <p>In April, Governor Tom Ridge released $150 million in state funding for the expansion.</p>

    <p>In May, the Southwestern Pennsylvania Convention Center Design Commission is formed to oversee the design of the new convention center.</p>

    <p>In July, the Public Auditorium Authority of Pittsburgh and Allegheny County (now the Sports and Exhibition Authority) along with the Southwestern Pennsylvania Convention Center Design Commission initiated a $750,000 international design competition funded by the Heinz Endowments and the Hillman, Benedum, McCune, Buhl and Richard King Mellon Foundations.</p>

    <h3>1999</h3>
    <p>The design proposal from Rafael Vinoly Architects is unanimously chosen after 25 design proposals were evaluated.</p>

    <h3>2000</h3>
    <p>In April, ground is broken for the new David L. Lawrence Convention Center. A three phase construction schedule is planned.</p>

    <h3>2002</h3>
    <p>In February, Phase I of the construction is complete as three exhibit halls open.</p>

    <p>In October, Phase II is complete adding another exhibit hall and 10 meeting rooms.</p>

    <h3>2003</h3>
    <p>Phase III is complete bringing the total number of exhibit halls to five, two lecture halls, 51 meeting rooms and a grand Ballroom.</p>

    <p>In September, the Grand Opening of the new David L. Lawrence Convention Center.
    </p>
    <h3>2011</h3>
    <p>In May 2011, the $9.5 million Convention Center Riverfront Plaza opened. Creating a “delta” at the base of the Water Feature, the Plaza connects the Convention Center to Point State Park and The Strip District through the Three Rivers Heritage Trail along the Allegheny River. </p>

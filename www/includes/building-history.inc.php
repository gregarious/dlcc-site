<p>What began as a vision by a group of determined government in 1990, corporate and non-profit foundation leaders became a reality in September 2003 with the official dedication ceremony of the David L. Lawrence Convention Center. The opening of the 1.5 million square foot building was so anxiously anticipated by the convention and exposition industry that the building hosted many large trade shows while still under construction.</p>

<p>From 2003 to 2012, the DLCC has hosted over 2,100 events with over 4.3 million visitors.</p>


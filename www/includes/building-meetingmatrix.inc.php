<h2>Don’t have MeetingMatrix&reg;: 2007?</h2>

<p>If you are a meeting planner, you are entitled to download and install the MeetingMatrix®: 2007 Silver Edition software free of charge. <a href="http://customers.meetingmatrix.com/Public/RequestSilver.aspx" target="_blank">Click here</a> to register for MeetingMatrix&reg; Silver</p>

<p>Once you have completed the installation of MeetingMatrix&reg; Silver, or if you already have the MeetingMatrix&reg; software, <a href="http://data.meetingmatrix.com/SpaceLinksNET/Default.aspx?Wrapper=MeetingMatrix&MMCode=CCPITDL" target="_blank">download</a> the Certified Room Diagram package for David L. Lawrence Convention Center. Once you have downloaded the diagram package, locate the file, open it, and follow the prompts for installation.</p>

